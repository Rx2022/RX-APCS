public class SimpleCounterButAForLoop
{
    public static void main(String[] args)
    {
    //the variable is 0
    //if the variable is less or equal to 300, it keeps adding one and printing
    for(int i=0;i<=300;i++)
    {
        System.out.println(i);
    }
    //the variable is 300
    //if the variable is more than 0, it keeps subtracting 1 and printing
    for(int i=300;i>=0;i--)
    {
        System.out.println(i);
    }
    //the variable is 4789
    //if the variable is less or equal to 5324, it keeps adding one and printing
    for(int i=4789;i<=5324;i++)
    {
        System.out.println(i);
    }
    //the variable is 5324
    //if the variable is less or equal to 4789, it keeps subtracting one and printing
    for(int i=5324;i>=4789;i--)
    {
        System.out.println(i);
    }
    }
}