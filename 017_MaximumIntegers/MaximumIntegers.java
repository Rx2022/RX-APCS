public class MaximumIntegers
{
    public static void main(String[] args)
    {
        Integer int1=new Integer(0);
        Integer max=new Integer(int1.MAX_VALUE);
        Integer min=new Integer(int1.MIN_VALUE);
        System.out.println(max);
        System.out.println(min);
        System.out.println(max+max+1000002);
        System.out.println(min+min+745);
    }
}